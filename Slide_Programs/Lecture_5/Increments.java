package CS2010.Lecture_5;

public class Increments {
	public static void main(String[] args){
	      int num = 0;
	      System.out.println(num);	
	      System.out.println(num++);
	      System.out.println(num);
	      System.out.println(++num);
	      System.out.println(num);
	      }
}
