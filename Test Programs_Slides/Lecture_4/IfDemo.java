package CS2010.Lecture_4;

public class IfDemo {
	public static void main(String[] args){
		int firstInt = 0;
		int secondInt = 1;
		
		if(firstInt < secondInt) System.out.println(firstInt + " < " + secondInt);
		if(firstInt == secondInt) System.out.println(firstInt + " = " + secondInt);
		if(firstInt > secondInt) System.out.println(firstInt + " > " + secondInt);
	}
}
